# Addition
print(15 + 7)
print("Hello " + "World!")
# Soustraction
print(15 - 7)
# Multiplication
print(15 * 7)
print("_-_" * 3)
# Division
print(15 / 7)
# Modulo (reste de la division Euclidienne)
print(15 % 7)
# Division Euclidienne
print(15 // 7)
# Exponentiation
print(15 ** 3)

# On peut concaténer ces opérateurs avec l'opérateur d'assignation (=)
print("\nAssignement:")
var = 3
var += 6 # <-> var = var + 6
print(var)

var **= 2
print(var)

var = "-"
var *= 50
print(var)
